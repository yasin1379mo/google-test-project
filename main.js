function toggle(){
        var x1 = document.getElementById("apps");

        if(x1.style.display === "none") {
                x1.style.display = "flex";
        }else{
                x1.style.display = "none";
        }
}
function showhide(){
        var x2 = document.getElementById("prof-menu");

        if(x2.style.display === "none") {
                x2.style.display = "inline";
        }else{
                x2.style.display = "none";
        }
}
